
mystr = "Sudarshan is a good boy"
print(len(mystr))   # length of String

print(mystr[0:10])  # print  0 to 9 character
print(mystr[:10])   #print 0 to 9
print(mystr[0:])    #print all String
print(mystr[::2])   #skip one one character
print(mystr[::3])   #skip two two character
print(mystr[::-1])  #reverse a String
print(mystr[::-2])  #reverse a String and escape one one character
print(mystr[-8:-1])  # - is same as + but - is in reverse order
print(mystr[15:22])  # this is same as above but it is +ve or above is -ve number



