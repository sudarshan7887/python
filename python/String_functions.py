mystr = "my name is sudarshan"
print(mystr.isalnum())                   #print true are false print true if string is numeric
print(mystr.isalpha())                   #print true are false print true if string is Alphanumeric
print(mystr.endswith("sudarshan"))      # print true if given string is true
print(mystr.count("s"))                 # count number of s in given String
print(mystr.find("is"))                 #find given string index number
print(mystr.capitalize())               # print first letter capital
print(mystr.lower())                    #convert given string in lower case
print(mystr.upper())                    #convert given string in upper case
print(mystr.replace("is","are"))        # replace is as are
